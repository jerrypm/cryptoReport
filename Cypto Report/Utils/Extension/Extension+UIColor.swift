import Foundation
import UIKit


extension UIColor {
    
    @nonobjc class var mainGreen: UIColor {
        return UIColor(red: 55/255, green: 139/255, blue: 113/255, alpha: 1)
    }
    
    @nonobjc class var dovegray: UIColor {
        return UIColor(white: 114.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var mineShaft: UIColor {
        return UIColor(white: 33.0 / 255.0, alpha: 1.0)
    }
    
}
